#include<stdio.h>
int main()
{	int x1,y1,x2,y2,x,y;
	scanf("%d %d\n%d %d\n%d %d",&x1,&y1,&x2,&y2,&x,&y);
	if(((x1<=x&&y2<=y)==1&&(x<=x2&&y<=y1)==1)==1)
	printf("IN");
	else
	printf("OUT");
	return 0;
}
/*题目描述
     二维平面点用（x,y）坐标表示。输入矩形的左上角和右下角坐标，输入一个点，判定该点是否在给定的矩形内。（假设该矩形的边分别平行于X轴、Y轴）。

输入
第一行：矩形的左上角坐标

第二行：矩形的右下角坐标

任一点坐标

输出
若该点落在矩形内，输出IN，否则，输出OUT 

样例输入
10  20
30  0
40  -10
样例输出
OUT*/
